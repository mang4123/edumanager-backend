router.post('/duvidas/:id/responder', (req: any, res) => {
  const { id } = req.params;
  const { resposta } = req.body;
  const professorId = req.user?.id;
  
  console.log('=== RESPONDER D√öVIDA (BIDIRECIONAL) ===');
  console.log('D√∫vida ID:', id);
  console.log('Professor ID:', professorId);
  console.log('Resposta:', resposta);
  
  // Encontrar e atualizar a d√∫vida no sistema global
  const duvidaIndex = req.estadoGlobal.duvidasSistema.findIndex((d: any) => d.id === parseInt(id));
  
  if (duvidaIndex !== -1) {
    req.estadoGlobal.duvidasSistema[duvidaIndex] = {
      ...req.estadoGlobal.duvidasSistema[duvidaIndex],
      status: 'respondida',
      resposta,
      dataResposta: new Date().toISOString()
    };
    
    const duvidaAtualizada = req.estadoGlobal.duvidasSistema[duvidaIndex];
    
    // Criar notifica√ß√£o para o aluno que fez a pergunta
    req.estadoGlobal.criarNotificacao(
      duvidaAtualizada.alunoId,
      'resposta',
      'Sua d√∫vida foi respondida!',
      `O professor respondeu sua pergunta sobre ${duvidaAtualizada.materia}`,
      'normal',
      {
        tipo: 'modal',
        dados: {
          duvidaId: parseInt(id),
          pergunta: duvidaAtualizada.pergunta,
          resposta: resposta
        }
      }
    );
    
    // Enviar email para o aluno (simulado)
    req.estadoGlobal.enviarNotificacaoEmail(
      'aluno@email.com',
      'Sua d√∫vida foi respondida - EduManager',
      `Ol√°! O professor respondeu sua pergunta sobre ${duvidaAtualizada.materia}:\n\nPergunta: ${duvidaAtualizada.pergunta}\nResposta: ${resposta}`
    );
    
    console.log('‚úÖ D√∫vida atualizada:', duvidaAtualizada);
  } else {
    // Fallback para d√∫vidas locais
    const duvidaLocalIndex = duvidasMemoria.findIndex(d => d.id === parseInt(id));
    if (duvidaLocalIndex !== -1) {
      duvidasMemoria[duvidaLocalIndex] = {
        ...duvidasMemoria[duvidaLocalIndex],
        status: 'respondida',
        resposta,
        dataResposta: new Date().toISOString()
      };
      console.log('‚úÖ D√∫vida local atualizada:', duvidasMemoria[duvidaLocalIndex]);
    } else {
      console.log('‚ùå D√∫vida n√£o encontrada');
    }
  }
  
  return res.json({
    message: 'D√∫vida respondida com sucesso',
    data: {
      duvidaId: parseInt(id),
      resposta,
      dataResposta: new Date().toISOString(),
      status: 'respondida',
      notificacaoEnviada: true
    }
  });
});

