// Validar convite
router.get('/convites/:token/validar', (req: AuthRequest, res) => {
  const { token } = req.params;
  
  console.log('=== VALIDAR CONVITE ===');
  console.log('Token:', token);
  
  try {
    // Decodificar token (simplificado para demonstra√ß√£o)
    const decoded = Buffer.from(token, 'base64').toString('utf-8');
    const [professorId, email, timestamp] = decoded.split('-');
    
    // Verificar se o convite n√£o expirou (7 dias)
    const dataGeracao = new Date(parseInt(timestamp));
    const agora = new Date();
    const diffDias = (agora.getTime() - dataGeracao.getTime()) / (1000 * 60 * 60 * 24);
    
    if (diffDias > 7) {
      return res.status(400).json({
        message: 'Convite expirado',
        data: { valido: false, motivo: 'Convite v√°lido por apenas 7 dias' }
      });
    }
    
    return res.json({
      message: 'Convite v√°lido',
      data: {
        valido: true,
        professorId,
        emailConvidado: email,
        diasRestantes: Math.ceil(7 - diffDias)
      }
    });
    
  } catch (error) {
    return res.status(400).json({
      message: 'Token de convite inv√°lido',
      data: { valido: false, motivo: 'Token malformado ou inv√°lido' }
    });
  }
});

